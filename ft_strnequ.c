#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int	i;

	i = 0;
	if (!s1 || !s2)
		return (0);
	if (*s1 == *s2 && *s1 == '\0')
		return (1);
	while (*s1 != '\0' && *s2 != '\0' && n > 0)
	{
		if (*s1 == *s2)
			i = 1;
		else
			return (0);
		s1++;
		s2++;
		n--;
	}
	return (i);
}
