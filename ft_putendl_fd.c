#include "libft.h"

void		ft_putendl_fd(char const *s, int fd)
{
	char	c;

	c = '\n';
	if (s != NULL && fd >= 0)
	{
		while (*s != '\0')
		{
			write(fd, s, 1);
			s++;
		}
		write(fd, &c, 1);
	}
}
